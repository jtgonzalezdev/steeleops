def normalize_sql(sql):
    return sql.replace('AUTOINCREMENT', 'GENERATED BY DEFAULT AS IDENTITY').replace('INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY', 'INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY')


class CursorWrapper:
    def __init__(self, cursor, backend):
        self.cursor = cursor
        self.backend = backend

    def _sql(self, sql):
        return sql.replace('?', '%s') if self.backend == 'postgres' else sql

    def execute(self, sql, params=None):
        self.cursor.execute(self._sql(sql), params or ())
        return self

    def executemany(self, sql, seq):
        self.cursor.executemany(self._sql(sql), seq)
        return self

    def executescript(self, script):
        if self.backend == 'postgres':
            for stmt in [s.strip() for s in script.split(';') if s.strip()]:
                self.cursor.execute(normalize_sql(stmt))
        else:
            self.cursor.executescript(script)
        return self

    def fetchone(self):
        return self.cursor.fetchone()

    def fetchall(self):
        return self.cursor.fetchall()

    def __iter__(self):
        return iter(self.cursor)

    def __getattr__(self, name):
        return getattr(self.cursor, name)


class ConnectionWrapper:
    def __init__(self, conn, backend):
        self.conn = conn
        self.backend = backend

    def cursor(self):
        return CursorWrapper(self.conn.cursor(), self.backend)

    def execute(self, sql, params=None):
        cur = self.cursor()
        cur.execute(sql, params or ())
        return cur

    def executemany(self, sql, seq):
        cur = self.cursor()
        cur.executemany(sql, seq)
        return cur

    def commit(self):
        return self.conn.commit()

    def rollback(self):
        return self.conn.rollback()

    def close(self):
        return self.conn.close()

    def __getattr__(self, name):
        return getattr(self.conn, name)

